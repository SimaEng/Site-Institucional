const closeMenu = () => {
  header.classList.remove("is-menu-open");
  menuToggle.setAttribute("aria-expanded", "false");
  menuToggle.setAttribute("aria-label", "Abrir menu");
  menuToggle.querySelector("i").className = "ri-menu-line";
  document.querySelector("#mobile-menu").setAttribute("aria-hidden", "true");
  document.body.classList.remove("is-menu-open");
};

const bindExpertise = () => {
  document.querySelectorAll("[data-expertise]").forEach((item) => {
    const button = item.querySelector("button");
    const panel = item.querySelector(".expertise-panel");
    const symbol = item.querySelector(".expertise-toggle");
    panel.removeAttribute("hidden");
    panel.setAttribute("aria-hidden", button.getAttribute("aria-expanded") !== "true");

    button.addEventListener("click", () => {
      const expanded = button.getAttribute("aria-expanded") === "true";
      document.querySelectorAll("[data-expertise]").forEach((other) => {
        other.querySelector("button").setAttribute("aria-expanded", "false");
        other.querySelector(".expertise-panel").setAttribute("aria-hidden", "true");
        other.querySelector(".expertise-toggle").className = "ri-add-line expertise-toggle";
      });
      if (!expanded) {
        button.setAttribute("aria-expanded", "true");
        panel.setAttribute("aria-hidden", "false");
        symbol.className = "ri-subtract-line expertise-toggle";
      }
    });
  });
};

const bindFilters = () => {
  const grid = document.querySelector("#insights-grid");
  if (!grid) return;
  document.querySelectorAll("[data-filter]").forEach((button) => {
    button.setAttribute("aria-pressed", button.classList.contains("is-active"));
    button.addEventListener("click", () => {
      if (button.classList.contains("is-active")) return;
      document.querySelectorAll("[data-filter]").forEach((item) => {
        item.classList.remove("is-active");
        item.setAttribute("aria-pressed", "false");
      });
      button.classList.add("is-active");
      button.setAttribute("aria-pressed", "true");
      const filter = button.dataset.filter;
      const filtered = filter === "todos" ? articles : articles.filter((article) => article.category === filter);

      const updateGrid = () => {
        grid.innerHTML = articleCards(filtered);
        grid.classList.remove("is-updating");
        observeReveals();
      };

      grid.classList.add("is-updating");
      if (document.startViewTransition) {
        document.startViewTransition(updateGrid);
      } else {
        window.setTimeout(updateGrid, 150);
      }
    });
  });
};

const observeReveals = () => {
  const elements = document.querySelectorAll(".reveal:not([data-observed])");
  const reducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (reducedMotion) {
    elements.forEach((element) => element.classList.add("is-visible"));
    return;
  }
  const observer = new IntersectionObserver((entries, currentObserver) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add("is-visible");
      currentObserver.unobserve(entry.target);
    });
  }, { threshold: 0.1, rootMargin: "0px 0px -30px" });
  elements.forEach((element, index) => {
    element.dataset.observed = "true";
    element.style.transitionDelay = `${Math.min((index % 4) * 60, 180)}ms`;
    observer.observe(element);
  });
};

const updateNavigation = (route) => {
  const activeRoute = route === "artigo" ? "conteudos" : route;
  navLinks.forEach((link) => {
    const isActive = link.dataset.nav === activeRoute;
    link.classList.toggle("is-active", isActive);
    if (isActive) link.setAttribute("aria-current", "page");
    else link.removeAttribute("aria-current");
  });
};

const beginRouteChange = () => {
  document.documentElement.classList.add("is-route-changing");
};

const forceRouteTop = () => {
  window.scrollTo(0, 0);
  document.documentElement.scrollTop = 0;
  document.body.scrollTop = 0;
};

const finishRouteChange = () => {
  requestAnimationFrame(() => {
    requestAnimationFrame(() => {
      document.documentElement.classList.remove("is-route-changing");
    });
  });
};

const renderRouteContent = () => {
  const { route, param } = parseRoute();
  let markup;
  let article;

  if (route === "artigo") {
    article = articles.find((item) => item.id === param);
    markup = article ? articleTemplate(article) : templates.conteudos();
  } else {
    markup = (templates[route] || templates.inicio)();
  }

  beginRouteChange();
  forceRouteTop();
  app.innerHTML = `<div class="route-view">${markup}</div>`;
  forceRouteTop();
  setTitle(route, article);
  updateNavigation(route);
  closeMenu();
  bindExpertise();
  bindFilters();
  observeReveals();
  requestAnimationFrame(() => {
    app.focus({ preventScroll: true });
    forceRouteTop();
  });
};

const renderRoute = (useTransition = false) => {
  renderRouteContent();
  finishRouteChange();
};

const setHeaderState = () => {
  header.classList.toggle("is-scrolled", window.scrollY > 24);
};

menuToggle.addEventListener("click", () => {
  const opening = menuToggle.getAttribute("aria-expanded") !== "true";
  header.classList.toggle("is-menu-open", opening);
  menuToggle.setAttribute("aria-expanded", String(opening));
  menuToggle.setAttribute("aria-label", opening ? "Fechar menu" : "Abrir menu");
  menuToggle.querySelector("i").className = opening ? "ri-close-line" : "ri-menu-line";
  document.querySelector("#mobile-menu").setAttribute("aria-hidden", String(!opening));
  document.body.classList.toggle("is-menu-open", opening);
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && header.classList.contains("is-menu-open")) {
    closeMenu();
    menuToggle.focus();
  }
});

document.addEventListener("click", (event) => {
  if (
    header.classList.contains("is-menu-open") &&
    !header.contains(event.target)
  ) {
    closeMenu();
  }
});

window.addEventListener("resize", () => {
  if (window.innerWidth > 860 && header.classList.contains("is-menu-open")) {
    closeMenu();
  }
});

window.addEventListener("scroll", setHeaderState, { passive: true });
window.addEventListener("popstate", () => renderRoute(true));

document.addEventListener("click", e => {
  const a = e.target.closest("a");
  if (a && a.href && !a.hasAttribute("target") && !a.getAttribute("href").startsWith("http") && !a.getAttribute("href").startsWith("mailto") && !a.getAttribute("href").startsWith("tel")) {
    const rawHref = a.getAttribute("href");
    if (rawHref.startsWith("/")) {
      e.preventDefault();
      history.pushState(null, "", rawHref);
      renderRoute(true);
    }
  }
});
